    private void txt_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idKeyPressed
        

        
        String id=txt_id.getText();
        try{
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){    
        Statement statement = konek.Koneksi().createStatement();
        String sql = "select * from makul where id_makul="+id+"";
        
        rs = statement.executeQuery(sql);
        if(rs.next())
        {
        combofak.setSelectedItem("fakultas");
        combojur.setSelectedItem("jurusan");
        combosem.setSelectedItem("Semester");
        txt_makul.setText(rs.getString("nama_makul"));
        txt_sks.setText(rs.getString("sks"));
        txt_dosen.setText(rs.getString("nama_dosen"));
        }
        else 
        {JOptionPane.showMessageDialog(null,"Mata Kuliah Belum ada di database","Konfirmasi",JOptionPane.INFORMATION_MESSAGE);
        }
        }
        
        
       }
       catch(Exception k)
       {
           
       }
    }//GEN-LAST:event_txt_idKeyPressed

