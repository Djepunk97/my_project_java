    private void button_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_simpanActionPerformed
        String id = txt_id.getText();
        String nama = txt_makul.getText();
        String sks = txt_sks.getText();
        String dosen = txt_dosen.getText();

        try {
            Statement statement;
            statement = (Statement) konek.Koneksi().createStatement();
            statement.executeUpdate("insert into makul values ('" + id + "','"+combofak.getSelectedItem()+"','"+combojur.getSelectedItem()+"','"+combosem.getSelectedItem()+"','" + nama + "','" + sks + "','" + dosen + "');");
            statement.close();
            txt_id.setText("");
            txt_makul.setText("");
            txt_sks.setText("");
            txt_dosen.setText("");
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "data belum masuk"+ex);
        }
    }//GEN-LAST:event_button_simpanActionPerformed

